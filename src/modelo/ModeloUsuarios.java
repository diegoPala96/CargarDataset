package modelo;

public class ModeloUsuarios {
int index;

int codigo;
int[] item;
double[] votos;
public int getIndex() {
	return index;
}
public void setIndex(int index) {
	this.index = index;
}
public int getCodigo() {
	return codigo;
}
public void setCodigo(int codigo) {
	this.codigo = codigo;
}
public int[] getItem() {
	return item;
}
public void setItem(int[] item) {
	this.item = item;
}
public double[] getVotos() {
	return votos;
}
public void setVotos(double[] votos) {
	this.votos = votos;
}

}